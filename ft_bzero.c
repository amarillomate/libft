#include "libft.h"

void ft_bzero(void *s, size_t n)
{
	unsigned char	*sn;
	size_t		i;
	
	sn = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		sn[i] = '\0';
		i++;
	}
}
