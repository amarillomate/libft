#include "libft.h"

char    *strrchr(const char *s , int c)
{
        int     i;

        i = ft_strlen(s);
        while(i >= 0)
        {
                if(s[i] == c)
                        return((char *)&s[i]);
                i--;
        }
        return (NULL);
}
